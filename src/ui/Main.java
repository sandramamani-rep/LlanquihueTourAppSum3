    /**
    * Valida los datos ingresados en el Formulario empleados
    * 
    * @return true si todos los datos son válidos;
    * false en caso contrario.
    */
    private boolean validarCamposEmpleado(String tipoEmpleado) {

        String rut = txtRut.getText();
        String nombre = txtNombre.getText();
        String telefono = txtTelefono.getText();
        String correo = txtCorreo.getText();
        String edad = txtEdad.getText();
        String calle = txtCalle.getText();
        String numero = txtNumero.getText();
        String ciudad = txtCiudad.getText();
        String region = txtRegion.getText();
        String sueldo = txtSueldo.getText();
        String fechaContratacion = txtFechaContratacion.getText();
        
        String idioma = txtIdioma.getText();
        String experiencia = txtExperiencia.getText();
        
        String empresa = txtEmpresa.getText();
        String especialidad = txtEspecialidad.getText();
        
        String turno = txtTurno.getText();

        if (validarParametro(rut)) {
            JOptionPane.showMessageDialog(this, "El campo RUT no puede estar vacío.", "Información", JOptionPane.INFORMATION_MESSAGE);
            
            return false;
        } else if (validarParametro(nombre)) {
            JOptionPane.showMessageDialog(this, "El campo Nombre no puede estar vacío.", "Información", JOptionPane.INFORMATION_MESSAGE);
            
            return false;
        } else if (validarParametro(telefono)) {
            JOptionPane.showMessageDialog(this, "El campo Teléfono no puede estar vacío.", "Información", JOptionPane.INFORMATION_MESSAGE);
            
            return false;
        } else if (validarParametro(correo)) {
            JOptionPane.showMessageDialog(this, "El campo Correo no puede estar vacío.", "Información", JOptionPane.INFORMATION_MESSAGE);
            
            return false;
        } else if (validarParametro(edad)) {
            JOptionPane.showMessageDialog(this, "El campo Edad no puede estar vacío.", "Información", JOptionPane.INFORMATION_MESSAGE);
            
            return false;
        } else if (validarParametro(calle)) {
            JOptionPane.showMessageDialog(this, "El campo Calle no puede estar vacío.", "Información", JOptionPane.INFORMATION_MESSAGE);
            
            return false;
        } else if (validarParametro(numero)) {
            JOptionPane.showMessageDialog(this, "El campo Número no puede estar vacío.", "Información", JOptionPane.INFORMATION_MESSAGE);
            
            return false;
        } else if (validarParametro(ciudad)) {
            JOptionPane.showMessageDialog(this, "El campo Ciudad no puede estar vacío.", "Información", JOptionPane.INFORMATION_MESSAGE);
            
            return false;
        } else if (validarParametro(region)) {
            JOptionPane.showMessageDialog(this, "El campo Región no puede estar vacío.", "Información", JOptionPane.INFORMATION_MESSAGE);
            
            return false;
        } else if (validarParametro(sueldo)) {
            JOptionPane.showMessageDialog(this, "El campo Región no puede estar vacío.", "Información", JOptionPane.INFORMATION_MESSAGE);
      
            return false;
        } else if (validarParametro(fechaContratacion)) {
            JOptionPane.showMessageDialog(this, "El campo Región no puede estar vacío.", "Información", JOptionPane.INFORMATION_MESSAGE);
            
            return false;
        }
        
        if (!validarEnteroPositivo(
            txtEdad.getText(),
            "La edad debe ser un número entero positivo.")) {
            return false;
        }
        if (!validarEnteroPositivo(
            txtNumero.getText(),
            "El número de la dirección debe ser un número entero positivo.")) {
            return false;
        }
        
        
        if(tipoEmpleado.equals("Guia turistico")){
            if (validarParametro(idioma)) {
                JOptionPane.showMessageDialog(this, "El campo Idioma no puede estar vacío.", "Información", JOptionPane.INFORMATION_MESSAGE);
                
                return false;
            } else if (validarParametro(experiencia)) {
                JOptionPane.showMessageDialog(this, "El campo Años de experiencia no puede estar vacío.", "Información", JOptionPane.INFORMATION_MESSAGE);
                
                return false;
            }
            if (!validarEnteroPositivo(
                txtExperiencia.getText(),
                "Los años de experiencia deben ser un número entero positivo.")) {
                return false;
            }
        } else if (tipoEmpleado.equals("Colaborador externo")){
            if (validarParametro(empresa)) {
                JOptionPane.showMessageDialog(this, "El campo empresa no puede estar vacío.", "Información", JOptionPane.INFORMATION_MESSAGE);
                
                return false;
            } else if (validarParametro(especialidad)) {
                JOptionPane.showMessageDialog(this, "El campo especialidad no puede estar vacío.", "Información", JOptionPane.INFORMATION_MESSAGE);
                
                return false;
            }
        }else if(tipoEmpleado.equals("Recepcionista")){
            if(validarParametro(turno)){
                JOptionPane.showMessageDialog(this, "El campo turno no puede estar vacío.");
                return false;
            }
        }
        return true;
    }
    
    /**
    * Valida los datos ingresados para registrar
    * un vehículo.
    *
    * @return true si los datos son válidos;
    * false en caso contrario.
    */
    private boolean validarCamposVehiculo() {

        String patente = txtPatente.getText();
        String tipo = txtTipo.getText();
        String capacidad = txtCapacidad.getText();

        if (validarParametro(patente)) {
            JOptionPane.showMessageDialog(this, "El campo Patente no puede estar vacío.", "Información", JOptionPane.INFORMATION_MESSAGE);
            
            return false;
        } else if (validarParametro(tipo)) {
            JOptionPane.showMessageDialog(this, "El campo Tipo de vehículo no puede estar vacío.", "Información", JOptionPane.INFORMATION_MESSAGE);
            
            return false;
        } else if (validarParametro(capacidad)) {
            JOptionPane.showMessageDialog(this, "El campo Capacidad no puede estar vacío.", "Información", JOptionPane.INFORMATION_MESSAGE);
            
            return false;
        }else if (!validarEnteroPositivo(
            txtCapacidad.getText(),
            "La capacidad debe ser un número entero positivo.")) {
            return false;
        }

        return true;
    }
    
    /**
    * Verifica que un campo no se encuentre vacío.
    *
    * @param campo texto ingresado por el usuario.
    * @return true si el campo está vacío; false en caso contrario.
    */
    private Boolean validarParametro(String campo){
        Boolean resultado = false;
        if(campo.isBlank()){
            resultado = true;
        }
        return resultado;
    }
    
    /**
    * Valida que el valor ingresado corresponda
    * a un número entero positivo.
    *
    * @param valor texto ingresado.
    * @param mensaje mensaje mostrado si la validación falla.
    * @return true si el número es válido; false en caso contrario.
    */
    private boolean validarEnteroPositivo(String valor, String mensaje) {

        try {

            int numero = Integer.parseInt(valor);

            if (numero <= 0) {
                JOptionPane.showMessageDialog(
                       this,
                       mensaje,
                       "Información",
                       JOptionPane.INFORMATION_MESSAGE);
                return false;
            }

            return true;

        } catch (NumberFormatException e) {

            JOptionPane.showMessageDialog(
                    this,
                    mensaje,
                    "Información",
                    JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
    }
    
