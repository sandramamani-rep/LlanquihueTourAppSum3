    /**
    * Habilita o deshabilita los campos del formulario
    * según el tipo de empleado seleccionado.
    */
    private void actualizarCampos() {

        String opcion = cmbTipoEmpleado.getSelectedItem().toString();

       
        if (opcion.equals("Guia turistico")) {

            txtIdioma.setEnabled(true);
            txtExperiencia.setEnabled(true);

            txtEmpresa.setEnabled(false);
            txtEspecialidad.setEnabled(false);

            txtTurno.setEnabled(false);

        }
        else if (opcion.equals("Colaborador externo")) {

            txtEmpresa.setEnabled(true);
            txtEspecialidad.setEnabled(true);

            txtIdioma.setEnabled(false);
            txtExperiencia.setEnabled(false);

            txtTurno.setEnabled(false);

        }
        else if (opcion.equals("Recepcionista")) {

            txtTurno.setEnabled(true);

            txtIdioma.setEnabled(false);
            txtExperiencia.setEnabled(false);

            txtEmpresa.setEnabled(false);
            txtEspecialidad.setEnabled(false);

        }
    }
   
