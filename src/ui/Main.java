    /**
    * Registra un nuevo vehículo utilizando
    * los datos ingresados en el formulario.
    *
    * @param evt evento generado por el botón.
    */
    private void btnAgregarVehiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarVehiculoActionPerformed
        String patente = txtPatente.getText();
        String tipo = txtTipo.getText();
        String capacidad = txtCapacidad.getText();
        
        if(validarCamposVehiculo() ){
            Vehiculo vehiculo = new Vehiculo(
                patente,
                tipo,
                Integer.parseInt(capacidad));

            gestorEntidades.agregarEntidad(vehiculo);
            limpiarFormularioVehiculo();
            JOptionPane.showMessageDialog( this,"Vehiculo agregado correctamente.", "Info", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarVehiculoActionPerformed
    
    /**
    * Actualiza el listado de entidades cuando
    * el usuario cambia a la pestaña correspondiente.
    *
    * @param evt evento de cambio de pestaña.
    */
    private void tabPrincipalStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_tabPrincipalStateChanged
        
        if (tabPrincipal.getSelectedIndex() == 2) {
            txtAreaResultado.setText(gestorEntidades.mostrarEntidades());
        }
        int indice = tabPrincipal.getSelectedIndex();
        String titulo = tabPrincipal.getTitleAt(indice);
        //System.out.println("prueba"+titulo);
        if (titulo.equalsIgnoreCase("Mostrar entidades")) {
            txtAreaResultado.setText(gestorEntidades.mostrarEntidades());
        }
        txtAreaResultado.setText(gestorEntidades.mostrarEntidades());

    }//GEN-LAST:event_tabPrincipalStateChanged

